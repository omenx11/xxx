def get_cluster(region_name: str) -> int:
    """Возвращает номер кластера для региона (0/1/2/3).

    Кластер 2 — индустриальные — default для всех, кто не попал в 0/1/3."""
    if region_name in CLUSTER_0:
        return 0
    if region_name in CLUSTER_1:
        return 1
    if region_name in CLUSTER_3:
        return 3
    return 2


def get_cluster_name(cluster_id: int) -> str:
    from .config import CLUSTER_NAMES
    return CLUSTER_NAMES.get(cluster_id, f"Кластер {cluster_id}")
